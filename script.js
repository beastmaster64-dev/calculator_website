

function addNums(num1, num2){
    return num1 + num2;
};

function subtNums(num1,num2){
    return num1 - num2;
};

function multiplyNums(num1,num2){
    return num1 * num2;
}

function divideNums(num1,num2){
    if(num2 == 0){
        return "Can't divide by zero!";
    }
    return num1/num2;
}



